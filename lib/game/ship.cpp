int getShipLaunchScore(const game::ShipT& ship) {
  // Sum all ship part scores, with a multiplier
  int acc = 0;
  for (int i = 0; i < ship.parts.size(); i++) {
    acc += ship.parts[i]->quality;
  }
  return acc * 2;
}

